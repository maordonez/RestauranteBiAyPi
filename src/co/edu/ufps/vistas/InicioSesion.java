    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = this.txtUsuario.getText();
        String clave = this.txtClave.getText();
        boolean tipoAcceso = this.rdCliente.isSelected();
        if (usuario.isEmpty() && clave.isEmpty() && !tipoAcceso) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los campos");
            return;
        }
        if (tipoAcceso) {
            JFrame contenedor = new ContenedorCliente();
            contenedor.setVisible(true);
            this.dispose();
        } else {
            boolean estado = ManagerPrincipal.getControlador().Ingresar(usuario, clave);
            if (estado) {
                JFrame contenedor;
                this.setVisible(false);
                switch (ManagerPrincipal.getControlador().consultarRol()) {
                    case "MESERO":
                        contenedor = new ContenedorMesero();
                        contenedor.setVisible(true);
                        break;
                    case "COCINERO":
                        contenedor= new ContenedorCocinero();
                        contenedor.setVisible(true);
                        break;
                    case "GERENTE":
                        contenedor = new ContenedorGerente();
                        contenedor.setVisible(true);
                        break;
                        
                }
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Datos invalidos");
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

